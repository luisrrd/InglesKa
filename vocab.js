// Text-to-speech using Web Speech API
let currentUtterance = null;
// ── UNIFIED VOICE ENGINE ──────────────────────────────────────
let selectedEnVoice = null;
let selectedEsVoice = null;

function populateVoiceList() {
  const voices = window.speechSynthesis.getVoices();
  const sel = document.getElementById('voice-en-select');
  if (!sel || voices.length === 0) return;

  // Filter English voices, prefer non-local (cloud/neural) when available
  const enVoices = voices.filter(v => v.lang.startsWith('en'));
  if (enVoices.length === 0) return;

  // Priority: Google voices > named female (Samantha, Karen, Moira, Fiona, Victoria, Serena) > any en-US > any en
  const priority = ['Google UK English Female','Google US English','Samantha','Karen','Moira','Fiona','Victoria','Serena','Zira','Hazel'];
  const sorted = [...enVoices].sort((a, b) => {
    const ai = priority.findIndex(p => a.name.includes(p));
    const bi = priority.findIndex(p => b.name.includes(p));
    if (ai === -1 && bi === -1) {
      // prefer en-US then en-GB
      if (a.lang === 'en-US' && b.lang !== 'en-US') return -1;
      if (b.lang === 'en-US' && a.lang !== 'en-US') return 1;
      return 0;
    }
    if (ai === -1) return 1;
    if (bi === -1) return -1;
    return ai - bi;
  });

  // Save current selection
  const currentName = sel.value;
  sel.innerHTML = '';
  sorted.forEach(v => {
    const opt = document.createElement('option');
    opt.value = v.name;
    opt.textContent = v.name.replace('Google ','').replace(' (Enhanced)','') + (v.lang ? ' ['+v.lang+']' : '');
    sel.appendChild(opt);
  });

  // Restore or pick best
  if (currentName && sorted.find(v => v.name === currentName)) {
    sel.value = currentName;
  } else {
    sel.selectedIndex = 0;
  }
  selectedEnVoice = sorted.find(v => v.name === sel.value) || sorted[0];
  sel.addEventListener('change', () => {
    selectedEnVoice = window.speechSynthesis.getVoices().find(v => v.name === sel.value);
  });
}

function getEsVoice() {
  const voices = window.speechSynthesis.getVoices();
  return voices.find(v => v.lang === 'es-ES') ||
         voices.find(v => v.lang === 'es-MX') ||
         voices.find(v => v.lang.startsWith('es')) || null;
}

function speakText(text, lang, onEnd) {
  if (!window.speechSynthesis) return;
  window.speechSynthesis.cancel();
  const utter = new SpeechSynthesisUtterance(text.replace(/\//g, ' ').replace(/ \/ /g, ' '));
  utter.lang = lang || 'en-US';
  utter.rate = 0.82;
  utter.pitch = 1;
  if (lang && lang.startsWith('es')) {
    const esV = getEsVoice();
    if (esV) utter.voice = esV;
  } else {
    if (!selectedEnVoice) populateVoiceList();
    if (selectedEnVoice) utter.voice = selectedEnVoice;
  }
  if (onEnd) { utter.onend = onEnd; utter.onerror = onEnd; }
  window.speechSynthesis.speak(utter);
}

function speakWord(text, lang, idx, type) {
  const btnId = type === 'en' ? `spk-en-${idx}` : `spk-es-${idx}`;
  document.querySelectorAll('.speak-btn, .speak-es-btn').forEach(b => b.classList.remove('playing'));
  const btn = document.getElementById(btnId);
  if (btn) btn.classList.add('playing');
  speakText(text, lang, () => { if (btn) btn.classList.remove('playing'); });
}

function speakWord2(text, lang, idx, type) {
  const btnId = type === 'en' ? `spk-en-${idx}` : `spk-es-${idx}`;
  document.querySelectorAll('.speak-btn, .speak-es-btn').forEach(b => b.classList.remove('playing'));
  const btn = document.getElementById(btnId);
  if (btn) btn.classList.add('playing');

  // 2. Detener cualquier audio previo
  if (window.currentAudioWord) {
    window.currentAudioWord.pause();
    window.currentAudioWord.currentTime = 0;
  }

  // 3. Configurar la URL de la API de Google (tl es el idioma, ej: 'en' o 'es')
  // Extraemos los primeros dos caracteres del lang (ej: 'en-US' -> 'en')
  const shortLang = lang.split('-')[0];
  const url = `https://translate.google.com/translate_tts?ie=UTF-8&q=${encodeURIComponent(text)}&tl=${shortLang}&client=tw-ob`;

  // 4. Crear y reproducir el objeto de audio
  window.currentAudioWord = new Audio(url);

  // 5. Quitar estado visual al terminar o por error
  window.currentAudioWord.onended = () => {
    if (btn) btn.classList.remove('playing');
  };

  window.currentAudioWord.onerror = () => {
    console.error("Error al cargar el audio de la API externa.");
    if (btn) btn.classList.remove('playing');
    // Opcional: Podrías llamar aquí a tu función antigua como 'fallback'
  };

  window.currentAudioWord.play().catch(e => {
    console.warn("Reproducción bloqueada o error de red:", e);
    if (btn) btn.classList.remove('playing');
  });
}


