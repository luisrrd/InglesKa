function buildVocabGrid(list) {
  const grid = document.getElementById('vocab-grid');
  document.getElementById('vocab-count').textContent = `${list.length} word${list.length !== 1 ? 's' : ''}`;
  if (!list.length) {
    grid.innerHTML = '<div class="no-results">No words found.</div>';
    return;
  }
  grid.innerHTML = list.map((w, i) => `
    <div class="vocab-card">
      <span class="cat-tag">adj</span>
      <div class="vocab-en">${w.en}</div>
      <div class="vocab-es">${w.es}</div>
      <div class="vocab-phonetic">${w.phonetic}</div>
      <div class="vocab-actions">
      	<button class="speak-btn" id="spk-en-${i}" onclick="speakWord('${w.en}','en-US',${i},'en')">
          <span class="sico">🔊</span> English
        </button>
        <button class="speak-es-btn" id="spk-es-${i}" onclick="speakWord('${w.es}','es-ES',${i},'es')">
          <span class="sico">🔊</span> Español
        </button>
      </div>
    </div>`).join('');
}

